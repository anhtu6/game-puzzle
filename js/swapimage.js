function swap1() {
    let x=Math.floor(Math.random()*3+1);
    if(x==2){
        document.getElementById('f1').src='img/monkey_part1x1.jpg';
    }else{
        if(x==3){
            document.getElementById('f1').src='img/panda_swap_part1x1.jpg';
        }else{
            document.getElementById('f1').src='img/funny-cat1_part1x1.jpg';
        }
    }
}
